from time import time
#  -----------------------------------------------------------------------------
def f(n):
    B=[1]
    for j in range(n-1):
        x=0
        B2=[]
        for i in range(len(B)):
            x = (x + B[-i-1]) % MOD
            B2.append(x)
        B = B2 + [B2[-1]]
    return(B[-1])
#  -----------------------------------------------------------------------------
MOD = 1020202009
LIMIT = 24680
for N in [4,6,8,100,1000,10000,LIMIT]:
    start = time()
    sol = f(N)
    if N == 4 or N == 8 or N == LIMIT:
        print('-'*60)
    print(f'Solution for N = {N:8,}: {sol:12,}, Run-Time: {time()-start:.2f}')
    if N == 4 or N == 8 or N == LIMIT:
        print('-'*60)
    N += 4
