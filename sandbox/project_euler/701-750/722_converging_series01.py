#  -----------------------------------------------------------------------------
#  Slowly converging series
#  Problem 722
#  https://projecteuler.net/problem=722
#  https://www.ivl-projecteuler.com/overview-of-problems/25-difficulty/problem-722
#  -----------------------------------------------------------------------------
from scipy.special import zeta
from math import factorial, log
#  -----------------------------------------------------------------------------
def euler_722():
    top = factorial(15)*zeta(16)
    bot = abs(log(1-2**(-25)))**16
    return top/bot
#  -----------------------------------------------------------------------------
res = euler_722()
print(round(res,12))
