from math import floor, factorial
from time import time
#  -----------------------------------------------------------------------------
def A(k,n):
    total = 0
    for a in range(floor(k/2)+1):
        total += factorial(k)//(factorial(a)*factorial(k-a))*\
                 factorial(k-a)//(factorial(a)*factorial(k-2*a))*\
                 (2**(k-2*a))**(n//k)

    return total % 1000000007
#  -----------------------------------------------------------------------------
modulo = 1000000007
for k,n in [(3,9),(4,20),(10**8,10**16)]:
    start = time()
    print(f'Solution for ({k:8},{n:17}): {A(k,n):}, Run-Time: {time()-start:.3f}')
