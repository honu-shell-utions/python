#  -----------------------------------------------------------------------------
#  Total Inversion Count of Divided Sequences
#  Problem 705
#  https://projecteuler.net/problem=705
#  -----------------------------------------------------------------------------
from sympy import primerange
import numpy as np
from time import time
#  -----------------------------------------------------------------------------
def get_A():
  A = np.zeros((9,8), dtype=np.int64)
  A[0,:] = 18, 18, 24, 18, 27, 18, 27, 24
  A[1,:] = 18, 36, 36, 36, 45, 36, 45, 48
  A[2,:] = 18, 18, 36, 36, 36, 36, 45, 36
  A[3,:] = 18, 36, 36, 54, 54, 54, 54, 60
  A[4,:] = 18, 18, 24, 18, 36, 36, 36, 36
  A[5,:] = 18, 36, 48, 54, 54, 72, 72, 72
  A[6,:] = 18, 18, 24, 18, 27, 18, 36, 36
  A[7,:] = 18, 36, 36, 54, 54, 54, 54, 72
  A[8,:] = 18, 18, 36, 36, 36, 36, 45, 36
  return A
#  -----------------------------------------------------------------------------
def euler_705(N):
  A = get_A()
  kLst = [ 0, 1, 2, 2, 3, 2, 4, 2, 4, 3]
  x = np.zeros(9, dtype=np.int64)
  K = 1
  res = 0
  for p in primerange(2,N):
    z = 0
    while p:
      p, d = divmod(p, 10)
      if d:
        z = 10*z + d
    while z:
      z, d = divmod(z, 10)
      res = (res*kLst[d] + x[d-1] * K) % MOD
      if d != 1:
        K = K * kLst[d] % MOD
        x += A[:,d-2]
  return res * ((MOD+1)//36) % MOD
#  -----------------------------------------------------------------------------
MOD = 10**9 + 7
for N in [20,50,10**8]:
    start = time()
    res = euler_705(N)
    print(f'Solution for N = {N:10}: {res:10}, Run-Time: {(time()-start)/60:6.3f} minutes.')
