#  -----------------------------------------------------------------------------
#  Summation of a Modular Formula
#  Problem 717
#  https://projecteuler.net/problem=717
#  -----------------------------------------------------------------------------
from sympy import primerange
from time import time
#  -----------------------------------------------------------------------------
def g(p):
  a = pow(2, p, p*(p-1))
  b = (a - p) % (p*(p-1))
  c = pow(2, a, p*p) // p
  d = pow(2, b, p*p) // p
  return (c - 2*d) % p
#  -----------------------------------------------------------------------------
for exp in range(2,8):
    start = time()
    total = 0
    for p in primerange(3,10**exp):
        total += g(p)
    print(f'Solution for n = 10^{exp}: {total:14}, Run-Time: {time()-start:8.3f}')
