#  -----------------------------------------------------------------------------
#  Duodigits
#  Problem 714
#  https://projecteuler.net/problem=714 
#  -----------------------------------------------------------------------------
from time import time
#  -----------------------------------------------------------------------------
def is_duodigit(n):
    set_n = set(str(n))
    return len(set_n) <= 2
#  -----------------------------------------------------------------------------
def d(n):
    mult = n
    while not is_duodigit(mult):
        mult += n
    return mult
#  -----------------------------------------------------------------------------
def euler_714(k):
    total = 0
    for n in range(1,k+1):
        total += d(n)
    return total
#  -----------------------------------------------------------------------------
for N in [110,150,500]:
    start = time()
    ans = euler_714(N)
    print(f'Solution for N = {N:6}: {ans:.12e}, Run-Time:{time()-start:.4f}')
