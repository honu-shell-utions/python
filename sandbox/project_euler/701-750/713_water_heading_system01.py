#  -----------------------------------------------------------------------------
#  Turán's water heating system
#  Problem 713
#  https://projecteuler.net/problem=713
#  -----------------------------------------------------------------------------
from math import floor
#  -----------------------------------------------------------------------------
def T(n, m):
    q, r = divmod(n, m-1)
    return (m-1)*q*(q-1)//2 + r*q
#  -----------------------------------------------------------------------------
def L(N):
    sum_all = 0
    for m in range(2,N+1):
        sum_all += T(N,m)
    return sum_all
#  -----------------------------------------------------------------------------
print('T(3,2)=',T(3,2))
print('T(8,4)=',T(8,4))
print('L(10**3)=',L(10**3))
print('L(10**7)=',L(10**7))
