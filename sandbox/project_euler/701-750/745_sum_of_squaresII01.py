#  -----------------------------------------------------------------------------
#  Sum of Squares II
#  Problem 745
#  https://projecteuler.net/problem=745
#  -----------------------------------------------------------------------------
from time import time
from sympy import divisors
from math import sqrt, isqrt
#  -----------------------------------------------------------------------------
def g(n):
    for d in reversed(divisors(n)):
        if sqrt(d) == isqrt(d):
            return d
#  -----------------------------------------------------------------------------
def S(N):
    total = 0
    for i in range(1,N+1):
        total += (g(i) % MOD)
    return total
#  -----------------------------------------------------------------------------
MOD = 1000000007
for exp in range(1,15):
    start = time()
    N = 10**exp
    print(f'Solution for n = 10^{exp:2}, {S(N):9}, Run-Time: {time()-start:>10.3f}')
