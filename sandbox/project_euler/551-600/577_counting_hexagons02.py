from math import sqrt
#  -----------------------------------------------------------------------------
def A236770():
    n = 1
    while True:
        yield n*(n + 1)*(3*n**2 + 3*n - 2)//8
        n += 1
#  -----------------------------------------------------------------------------
def A228317():
    n = 3
    while True:
        yield n*(n - 1)*(n - 2)*(3*n - 5)//8
        n += 1
#  -----------------------------------------------------------------------------
def A000914():
    n = 1
    while True:
        yield (n+1)*n*(n+2)*(3*n+5)//24
        n += 1
#  -----------------------------------------------------------------------------
limit = 12345
total = 0
g770 = A236770()
g317 = A228317()
g914 = A000914()
for n in range(3,limit + 1):
    if n % 3 == 0:
        total += next(g770)
    elif n % 3 == 1:
        total += next(g317)
    else:
        total += 3*next(g914)
        
print(f'Solution: {total}')
