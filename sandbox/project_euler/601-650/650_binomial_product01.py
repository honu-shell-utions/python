#  -----------------------------------------------------------------------------
#  Divisors of Binomial Product
#  Problem 650
#  https://projecteuler.net/problem=650
#  -----------------------------------------------------------------------------
from math import factorial
from sympy import divisors
#  -----------------------------------------------------------------------------
def cnr(n,r):
    return factorial(n)//(factorial(r)*factorial(n-r))
#  -----------------------------------------------------------------------------
def B(n):
    prod = 1
    for r in range(n+1):
        prod *= cnr(n,r)
    return prod
#  -----------------------------------------------------------------------------
def D(n):
    return sum(divisors(B(n)))
#  -----------------------------------------------------------------------------
def S(n):
    div_sum = 0
    for j in range(1,n+1):
        div_sum += D(j)
    return div_sum
#  -----------------------------------------------------------------------------
print(S(5))
print(S(10))
