#  -----------------------------------------------------------------------------
#  Divisibility streaks
#  Problem 601
#  https://projecteuler.net/problem=601
#  -----------------------------------------------------------------------------
from time import time
#  -----------------------------------------------------------------------------
def streak(n):
    k = 1
    while (n+k) % (k+1) == 0:
        k += 1
    return k
#  -----------------------------------------------------------------------------
def P(s,N):
    count = 0
    for n in range(2,N):
        if streak(n) == s:
            count += 1
    return count
#  -----------------------------------------------------------------------------
print(streak(13))
print(streak(120))
print(P(3,14))
print(P(6,10**6))
