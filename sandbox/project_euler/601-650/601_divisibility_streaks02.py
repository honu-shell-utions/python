#  -----------------------------------------------------------------------------
#  Divisibility streaks
#  Problem 601
#  https://projecteuler.net/problem=601
#  -----------------------------------------------------------------------------
from math import lcm, floor
#  -----------------------------------------------------------------------------
def P(s,N):
    lst01 = [x for x in range(1,s+1)]
    lcm01 = lcm(*lst01)
    lcm02 = lcm(s+1,lcm01)
    return floor((N-2)/lcm01) - floor((N-2)/lcm02)
#  -----------------------------------------------------------------------------
print('P(3,14) =',P(3,14))
print('P(6,10**6) =',P(6,10**6))
total = 0
for i in range(1,32):
    total += P(i,4**i)
print('Solution:',total)
