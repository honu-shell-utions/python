#-------------------------------------------------------------------------------
from math import sqrt
from time import time
#-------------------------------------------------------------------------------
def euler_199():
    size = 10
    k1 = 1
    k0 = k1 * (3 - 2 * sqrt(3))
    a0 = 1 / (k0 * k0)
    a1 = 3 / (k1 * k1)
    a1 += 3 * getArea(k0, k1, k1, size)
    a1 += getArea(k1, k1, k1, size)
    result = (a0 - a1) / a0
    return result
#-------------------------------------------------------------------------------
def getArea(k1,k2,k3,depth):
    if depth == 0:
        return 0.0
    k4 = k1 + k2 + k3 + 2 * sqrt(k1 * k2 + k2 * k3 + k3 * k1)
    a = 1 / (k4 * k4)
    a += getArea(k1, k2, k4, depth - 1)
    a += getArea(k2, k3, k4, depth - 1)
    a += getArea(k3, k1, k4, depth - 1)
    return a
#-------------------------------------------------------------------------------
start = time()
answer = round(euler_199(),8)
print(f'Solution: {answer},Run-Time: {time()-start}')
