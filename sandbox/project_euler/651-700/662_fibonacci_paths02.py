#  -----------------------------------------------------------------------------
#  Fibonacci paths
#  Problem 662
#  https://projecteuler.net/problem=662
#  -----------------------------------------------------------------------------
def p662(w,h):
    dp = [[0 for j in range(h+1)] for i in range(w+1)]
    dp[0][0] = 1 
 
    for i in range(w+1):
        for j in range(h+1):
            for a,b in valid:
                if a > i:
                    break
                if b <= j:
                    dp[i][j] = (dp[i][j] + dp[i-a][j-b]) % M
            for a,b in valid:
                if a > j:
                    break
                if b <= i:
                    dp[i][j] = (dp[i][j] + dp[i-b][j-a]) % M

    return dp[w][h]
