#  -----------------------------------------------------------------------------
#  Powers of Two 
#  Problem 686
#  https://projecteuler.net/problem=686
#  -----------------------------------------------------------------------------
from math import log10, floor
from time import time
#  -----------------------------------------------------------------------------
def p(L,n):
    str_L = str(L)
    j = 1
    count = 0
    while True:
        res = 2**j
        if str_L == str(res)[:len(str_L)]:
            count += 1
            if count == n:
                return j
        j += 1
#  -----------------------------------------------------------------------------
def euler_686(n):
    bot = log10(1.23)
    top = log10(1.24)
    j = 0
    count = 0
    while count < n:
        j += 1
        if bot < log10(2**j) - floor(log10(2**j)) < top:
            count += 1
    return j
#  -----------------------------------------------------------------------------
print(p(12,1))
print(p(12,2))
print(euler_686(45))
start = time()
print(f'Solution: {euler_686(678910)}, Run-Time: {time()-start:.3f}')
