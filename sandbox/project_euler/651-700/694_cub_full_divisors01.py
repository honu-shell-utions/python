#  -----------------------------------------------------------------------------
#  Cube-full Divisors
#  Problem 694
#  https://projecteuler.net/problem=694
#  -----------------------------------------------------------------------------
from sympy import divisors, primefactors
from time import time
#  -----------------------------------------------------------------------------
def is_cube_full(n):
    if n == 1:
        return True
    for p in primefactors(n):
        if n % p**3 != 0:
            return False
    return True
#  -----------------------------------------------------------------------------
def s(n):
    cube_full_divisors = 0
    for d in divisors(n):
        if is_cube_full(d):
            cube_full_divisors += 1
    return cube_full_divisors
#  -----------------------------------------------------------------------------
def S(n):
    total = 0
    for i in range(1,n+1):
        total += s(i)
    return total
#  -----------------------------------------------------------------------------
for N in [16,10**2,10**4,10**5,10**6,10**7]:
    start = time()
    print(f'Solution for N = {N:19}: {S(N):19}, Run-Time: {time()-start:.3f}')
