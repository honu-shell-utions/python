#  -----------------------------------------------------------------------------
#  Largest prime
#  Problem 659
#  https://projecteuler.net/problem=659
#  -----------------------------------------------------------------------------
from sympy import primefactors
from time import time
#  -----------------------------------------------------------------------------
for exp in range(2,8):
    start = time()
    limit = 10**exp
    total = 0
    for k in range(1,limit+1):
        total += primefactors(4*k**2+1)[-1]
    print(f'For n = 10^{exp}: {total % 10**18:>18}, Run-Time: {time()-start}')
