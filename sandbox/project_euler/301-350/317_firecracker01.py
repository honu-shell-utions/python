from math import pi
#  -----------------------------------------------------------------------------
def volume(v,h):
    g = 9.81
    V = pi/2 * (v**2/(2*g)+h)**2/(g/(2*v**2))
    return round(V,4)
#  -----------------------------------------------------------------------------
print(volume(20,100))
