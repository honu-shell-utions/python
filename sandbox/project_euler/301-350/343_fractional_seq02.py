from time import time
from sympy import primefactors
#  -----------------------------------------------------------------------------
def f(n):
    return primefactors(n+1)[-1] - 1
#  -----------------------------------------------------------------------------
def S(n):
    total = 0
    for k in range(1,n+1):
        total += f(k**3)
    return total
#  -----------------------------------------------------------------------------
for n in [20,1,2,3]:
    print(f'f({n}) = {f(n)}')
for n in [100,2*10**6]:
    start = time()
    print(f'Solution for n = {n:7}: {S(n):19}, Run-Time: {(time()-start)/60:8.2f} minutes.')
