from time import time
from sympy import primefactors
#  -----------------------------------------------------------------------------
# factor k**3 + 1
# k**3 + 1 = (k + 1)(k**2 - k + 1)
def S(n):
    total = 1
    for k in range(2,n+1):
        total += (max(primefactors(k+1)[-1],primefactors(k**2-k+1)[-1])) - 1
    return total
#  -----------------------------------------------------------------------------
for n in [100,2*10**6]:
    start = time()
    print(f'Solution for n = {n:7}: {S(n):19}, Run-Time: {(time()-start)/60:8.2f} minutes.')
