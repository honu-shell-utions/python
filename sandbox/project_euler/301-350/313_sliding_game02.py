#  -----------------------------------------------------------------------------
# Sliding game 
# Problem 313
# https://projecteuler.net/problem=313
#  -----------------------------------------------------------------------------
from sympy import primerange
#  -----------------------------------------------------------------------------
def euler_313(P):
    tot = 2
    for p in primerange(3,P):
        j = (p - 1)//2
        tot += j*(j + 1)//3
    print(P, tot)

euler_313(10**2)
euler_313(10**6)
