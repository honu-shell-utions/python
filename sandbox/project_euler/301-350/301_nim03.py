from time import time
#  -----------------------------------------------------------------------------
def fibonacci_gen():
    T1 = T2 = 1
    yield T1
    yield T2
    while True:
        yield T1+T2
        T1,T2 = T2,T1+T2
#  -----------------------------------------------------------------------------
for exp in range(2,31):
    start = time()
    fib = fibonacci_gen()
    for _ in range(exp+2):
        f = next(fib)
    print(f'Solution for 2^{exp:2}: {f:8}, Run-Time: {time()-start:8.3f}')
