from random import randint
from time import time
#  -----------------------------------------------------------------------------
def nim_sum(h01,h02,h03):
    return h01 ^ h02 ^ h03
#  -----------------------------------------------------------------------------
for exp in range(2,31):
    start = time()
    count = 0
    for x in range(2**exp):
        if nim_sum(x,2*x,3*x) == 0:
            count += 1
    print(f'Solution for 2^{exp:2}: {count:8}, Run-Time: {time()-start:8.3f}')
