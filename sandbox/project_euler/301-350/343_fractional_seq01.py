from time import time
from math import gcd
#  -----------------------------------------------------------------------------
def f(n):
    num = 1
    den = n
    while den != 1:
        num += 1
        den -= 1
        f = gcd(num,den)
        num //= f
        den //= f
    return num
#  -----------------------------------------------------------------------------
def S(n):
    total = 0
    for k in range(1,n+1):
        total += f(k**3)
    return total
