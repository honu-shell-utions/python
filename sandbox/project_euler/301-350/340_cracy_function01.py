def F(n):
    q, r = divmod(b-n, a)
    return b + (a-c) * (4+3*q) - r
#  -----------------------------------------------------------------------------    
def S():
    Q, R = divmod(b, a)
    s = Q * a * (2*F(b) + 3*(a-c)*(Q-1) - (a-1)) // 2
    startseg = (F(0) + F(R)) * (R+1) // 2
    return s + startseg
#  -----------------------------------------------------------------------------
a, b, c = 50, 2000, 40
print(F(0))
print(F(2000))
print(S())
a, b, c = 21**7, 7**21, 12**7
print(S() % 10**9)
