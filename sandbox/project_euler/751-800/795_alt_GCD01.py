#  -----------------------------------------------------------------------------
#  Alternating GCD Sum
#  Problem 795
#  https://projecteuler.net/problem=795
#  -----------------------------------------------------------------------------
from math import gcd
from time import time
#  -----------------------------------------------------------------------------
#see https://oeis.org/A353909
def g(n):
    return sum((-1)**k*gcd(n, k*k) for k in range(1, n+1))
#  -----------------------------------------------------------------------------
def G(N):
    total = 0
    for n in range(2,N+1,2):
        total += g(n)
    return total -(N//2)**2
#  -----------------------------------------------------------------------------
start = time()
print(g(4))
print(g(1234))
print(G(1234))
print(f'Solution: {G(12345678)}, Run-Time: {time()-start}')
