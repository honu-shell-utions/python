#  -----------------------------------------------------------------------------
#  Fermat Equation
#  Problem 753
#  https://projecteuler.net/problem=753
#  -----------------------------------------------------------------------------
from sympy import primerange
from time import time
#  -----------------------------------------------------------------------------
def get_primes(n):
    return list(primerange(2,n+1))
#  -----------------------------------------------------------------------------
def F(p):
    if p == 3:
        return 2
    if p % 3 == 2:
        return (p-1)*(p-2)
    
    for a in range(int(2*p**0.5),0,-1):
        b = int(((4*p-a**2)//27)**0.5)
        if 4*p == a**2+27*b**2:
            break
    if a % 3 == 1:
        N1 = p+a-2
    else:
        N1 = p-a-2
        
    return (N1-6)*(p-1)
#  -----------------------------------------------------------------------------
for LIMIT in [5,7,10**2,10**3,10**4,6*10**6]:
    start = time()
    total = 0
    for p in get_primes(LIMIT):
        total += F(p)
    print(f'Solution for n = {LIMIT:7}: {total:20}, Run-Time: {time()-start:.3f}')
