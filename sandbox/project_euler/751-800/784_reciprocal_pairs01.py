#  -----------------------------------------------------------------------------
#  Reciprocal Pairs
#  Problem 784
#  https://projecteuler.net/problem=784
#  -----------------------------------------------------------------------------
from sympy import divisors
from time import time
#  -----------------------------------------------------------------------------
def F(N):
    total = 0
    for p in range(2,N+1):
        if p % 10**5 == 0 :
            print(p)
        divs = divisors(p**2-1)
        for t in divs:
            if t > 2*p:
                total += t
    return total               
#  -----------------------------------------------------------------------------
for limit in [5,10**2,2*10**6]:
    start = time()
    solution = F(limit)
    print(f'Solution: {solution}, Run-Time: {time()-start}')
