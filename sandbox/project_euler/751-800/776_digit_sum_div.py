#   ----------------------------------------------------------------------------
#   Digit Sum Division
#   Problem 776
#   https://projecteuler.net/problem=776
#   ----------------------------------------------------------------------------
def sum_digits(n):
    str_n = str(n)
    total = 0
    for d in str_n:
        total += int(d)
    return total
#   ----------------------------------------------------------------------------
def F(n):
    total = 0
    for k in range(1,n+1):
        total += k/sum_digits(k)
    return total
#   ----------------------------------------------------------------------------
print(F(123))
print(F(12345))
print(F(1234567890123456789))
