#  Ruff Numbers
#  Problem 773
#  https://projecteuler.net/problem=773
#  -----------------------------------------------------------------------------
from sympy import nextprime
#  -----------------------------------------------------------------------------
def euler_773(K, M):
    k = 0
    F = [1, 7, 9, 3]  #F_d(k) at k=0, for d=1, 7, 9, 3
    n, p = 5, 7       #N(0)/2, p prime
    while k < K:
        if p % 10 == 7:
            k += 1
            n *= p*(p-1)
            F  = [n + p*(F[i]-F[i+3]) for i in range(-4, 0)]
        p  = nextprime(p)
    return F[1] % M   #Sum of numbers ending 7 at k=K
#  -----------------------------------------------------------------------------
for K in [3,4,5,97]:
    print(K,euler_773(K, 1000000007))
