#  -----------------------------------------------------------------------------
#  A squared recurrence relation
#  Problem 759
#  https://projecteuler.net/problem=759
#  -----------------------------------------------------------------------------
def f(n):
    if n < 1:
        return 0
    if n == 1:
        return 1
    if n % 2 == 0:
        k = n // 2
        return 2 * f(k)
    else:
        k = (n - 1) // 2
        return 2*k + 1 + 2 * f(k) + f(k) // k
#  -----------------------------------------------------------------------------
def S(n):
    total = 0
    for i in range(1,n+1):
        total += f(i)**2
    return total % MOD
#  -----------------------------------------------------------------------------
MOD = 10**9 + 7
for exp in range(1,17):
    print(exp,S(10**exp) % MOD)
