#  -----------------------------------------------------------------------------
#  Product of Gauss Factorials
#  Problem 754
#  https://projecteuler.net/problem=754
#  -----------------------------------------------------------------------------
from math import gcd
#  -----------------------------------------------------------------------------
def g(n):
    gf = 1
    for k in range(1,n+1):
        if gcd(k,n) == 1:
            gf *= k
    return gf % MOD
#  -----------------------------------------------------------------------------
def G(n):
    pgf = 1
    for k in range(1,n+1):
        pgf *= g(k)
    return pgf % MOD
#  -----------------------------------------------------------------------------
MOD = 10**9+7
for limit in range(100,3*10**3+1,10**2):
    print(limit,G(limit))
