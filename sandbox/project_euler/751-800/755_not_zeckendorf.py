from functools import lru_cache
#-------------------------------------------------------------------------------
def fib_gen():
    c1 = 1
    c2 = 2
    yield c1
    yield c2
    while True:
        yield c1+c2
        c1,c2 = c2,c1+c2
#-------------------------------------------------------------------------------
def make_fibs(n):
    temp = [1]
    for f in fib_gen():
        if temp[-1] > n:
            break
        temp.append(f)
    return temp[1:-1]
#-------------------------------------------------------------------------------
@lru_cache(maxsize=64)
def S(n):
    if n < 2:
        return n+1
    fibs = make_fibs(n)
    c = len(fibs)
    if n >= 2 * fibs[-2]:
        return 2**(c-2) + S(n - fibs[-1]) + S(n - fibs[-2]) - S(n - 2 * fibs[-2])
    else:
        return 2**(c-2) + S(n - fibs[-1]) + S(n - fibs[-2])
#-------------------------------------------------------------------------------
print(S(10**2))
print(S(10**4))
print(S(10**13))
