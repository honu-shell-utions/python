#  -----------------------------------------------------------------------------
#  A squared recurrence relation
#  Problem 759
#  https://projecteuler.net/problem=759
#  https://oeis.org/A245788
#  -----------------------------------------------------------------------------
from functools import cache
from math import comb
#  -----------------------------------------------------------------------------
@cache
def S(n, a=2, b=2):
    if n <= 1:
        return n
    result = 1 + 2 ** a * S(n // 2, a, b)
    for i in range(a + 1):
        for j in range(b + 1):
            result += 2 ** i * comb(a, i) * comb(b, j) * S((n - 1) // 2, i, j)
    return result
#  -----------------------------------------------------------------------------
MOD = 10**9 + 7
for exp in range(1,17):
    print(exp,S(10**exp) % MOD)
