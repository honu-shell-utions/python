#  -----------------------------------------------------------------------------
#  Alternating GCD Sum
#  Problem 795
#  https://projecteuler.net/problem=795
#  -----------------------------------------------------------------------------
from math import gcd
from time import time
#  -----------------------------------------------------------------------------
#see https://oeis.org/A353909
def g(n):
        return sum((-1)**k*gcd(n, k*k) for k in range(1, n+1))
#  -----------------------------------------------------------------------------
def G(N):
        total = 0
        for n in range(1,N//2+1):
                total += g(2*n)
                if n % 10**4 == 0:
                        print('Current n:',n,'Current Total:',total)
        if N % 2 == 1:
                return total-((N+1)//2)**2 + N
        else:
                return total-((N+1)//2)**2
#  -----------------------------------------------------------------------------
print('-'*70)
for N in [1234,12345678]:
    start = time()
    if N == 12345678:
        print('-'*70)
    print(f'Solution for N = {N:10}: {G(N):18}, Run-Time: {time()-start:8.3f}')
print('-'*70)
