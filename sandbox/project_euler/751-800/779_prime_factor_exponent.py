#  -----------------------------------------------------------------------------
#  Prime Factor and Exponent 
#  Problem 779
#  https://projecteuler.net/problem=779
#  -----------------------------------------------------------------------------
from sympy import primerange
from time import time
#  -----------------------------------------------------------------------------
def getsumf():
    total = 0
    curr = 1
    for p in primerange(2,10**6):
        total += curr*(1/((p*(p-1)**2)))
        curr *= (p-1)/p
    return round(total,12)
#  -----------------------------------------------------------------------------
start = time()
print(f'Solution: {getsumf()}, Run-Time: {time()-start}')
