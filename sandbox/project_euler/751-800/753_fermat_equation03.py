#  -----------------------------------------------------------------------------
#  Fermat Equation
#  Problem 753
#  https://projecteuler.net/problem=753
#  -----------------------------------------------------------------------------
from time import time
from sympy import primerange
#  -----------------------------------------------------------------------------
def F(N):
    A=[6] * (N+1)
    for b in range(1,int((4*N/27)**0.5)+1):
        bb=27*b*b
        for a in range(b%2,int((4*N-bb)**0.5)+1,2):
            p=(bb+a*a)//4
            if a % 3 == 1:
                A[p] = a
            else:
                A[p] = -a

    s = sum((p-1)*(p-8+A[p]) for p in primerange(1,N))
    return s
#  -----------------------------------------------------------------------------
for LIMIT in [5,7,10**2,10**3,10**4,6*10**6]:
    start = time()
    total = F(LIMIT)
    print(f'Solution for n = {LIMIT:7}: {total:20}, Run-Time: {time()-start:.3f}')
