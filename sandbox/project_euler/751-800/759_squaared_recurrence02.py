#  -----------------------------------------------------------------------------
#  A squared recurrence relation
#  Problem 759
#  https://projecteuler.net/problem=759
#  https://oeis.org/A245788
#  -----------------------------------------------------------------------------
def f(n):
    return [k*bin(k)[2:].count('1') for k in range(n+1)]
#  -----------------------------------------------------------------------------
def S(n):
    vals = f(n)
    total = 0
    for v in vals:
        total += v**2
    return total % MOD
#  -----------------------------------------------------------------------------
MOD = 10**9 + 7
for exp in range(1,17):
    print(exp,S(10**exp) % MOD)
