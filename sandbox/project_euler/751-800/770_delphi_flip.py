from time import time
from math import pi, sqrt, ceil
#  -----------------------------------------------------------------------------
start = time()
target = 2/1.9999 - 1
n = 0
f = 1
while target < f:
    n += 1
    f *= (2*n-1)/(2*n)
print("\nAnswer: ", n)
print("Time  : ", time() - start)
