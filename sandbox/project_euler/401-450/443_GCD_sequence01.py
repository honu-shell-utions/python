from time import time
from sympy import isprime
#  -----------------------------------------------------------------------------
def g(x):
    n = 17
    gn = 51
    while True:
        if isprime(2*n-1):
            k = n-1
        else:
            k = 1
            while (2*n-1)%(2*k+1):
                k += 1
        n += k
        if n >= x:
            return 2*(n-k)+x
#  -----------------------------------------------------------------------------
for exp in range(2,16):
    start = time()
    N = 10**exp
    print(f'For n = 10^{exp:2}: {g(N):17}, Run-Time: {time()-start:.3f}')
