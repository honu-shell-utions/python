#  -----------------------------------------------------------------------------
#  Lattice points enclosed by parabola and line
#  Problem 403
#  https://projecteuler.net/problem=403
#  -----------------------------------------------------------------------------
from math import isqrt, sqrt
#  -----------------------------------------------------------------------------
def ab_pairs(N):
    pairs = []
    for a in range(-N,N+1):
        for b in range(-N,N+1):
            disc = a**2+4*b
            if disc < 0:
                continue
            if sqrt(disc) == isqrt(disc):
                pairs.append((a,b))
    return pairs
#  -----------------------------------------------------------------------------
def L(pair):
    a, b = pair
    count = 0
    disc = isqrt(a**2+4*b)
    left = (a - disc)//2
    right = (a + disc)//2
    for x in range(left,right+1):
        count += line(pair,x) - parabola(x) + 1
    return count
#  -----------------------------------------------------------------------------
def line(pair,x):
    a,b = pair
    return a*x+b
#  -----------------------------------------------------------------------------
def parabola(x):
    return x**2
#  -----------------------------------------------------------------------------
def S(N):
    big_total = 0
    pairs = ab_pairs(N)
    for p in pairs:
        big_total += (L(p) % MOD)
    return big_total % MOD
#  -----------------------------------------------------------------------------
MOD = 10**8
for N in [5,10**2,10**4,10**8,10**12]:
    print(f'For N = {N:12}: {S(N):12}')
