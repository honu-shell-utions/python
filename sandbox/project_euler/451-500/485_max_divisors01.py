#  -----------------------------------------------------------------------------
#  Maximum number of divisors
#  Problem 485
#  https://projecteuler.net/problem=485
#  -----------------------------------------------------------------------------
from sympy import divisors
from time import time
#  -----------------------------------------------------------------------------
def d(n):
    return len(divisors(n))
#  -----------------------------------------------------------------------------
def M(n,k):
    max_divs = -(10**6)
    for j in range(n,n+k):
        nd = d(j)
        if nd > max_divs:
            max_divs = nd
    return max_divs
#  -----------------------------------------------------------------------------
def S(u,k):
    total = 0
    for n in range(1,u-k+2):
        total += M(n,k)
    return total
#  -----------------------------------------------------------------------------
for a,b in [(10**3,10),(10**8,10**5)]:
    start = time()
    print(f'Solution for {a,b}: {S(a,b)}, Run-Time: {time()-start}')            
