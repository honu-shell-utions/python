from sympy import divisors
from time import time
#  -----------------------------------------------------------------------------
def S(u, k):
  z = [0 for i in range(10**3)]
  s, m = 0, 0
  for n in range(1,u+1):
    d = len(divisors(n))
    z[d] = n
    if d > m:
        m = d 
    while z[m] <= n - k:
        m -= 1
    if n >= k:
        s += m
  return s
#  -----------------------------------------------------------------------------
for a,b in [(10**3,10),(10**8,10**5)]:
    start = time()
    print(f'Solution for {a,b}: {S(a,b)}, Run-Time: {time()-start}')            
