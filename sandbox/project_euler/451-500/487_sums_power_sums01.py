#  -----------------------------------------------------------------------------
#  Sums of power sums
#  Problem 487
#  https://projecteuler.net/problem=487
#  -----------------------------------------------------------------------------
from sympy import primerange
from time import time
#  -----------------------------------------------------------------------------
start = time()
power = 10000
N = 10**12
total = 0
for p in primerange(2*(10**9), 2*(10**9) + 2000 + 1):
    A = 0
    B = 0
    for ix in range((N % p)+1,p):
        A -= pow(ix,power,p)
        B -= pow(ix,power+1,p)
    total += ((N+1)*A - B) % p

print("Answer: ", total)
print("Time: ", time() - start)
