from scipy import integrate
import numpy as np
#  -----------------------------------------------------------------------------
def f(a,b,theta):
        x = a**2*np.cos(theta)**2+b**2*np.sin(theta)**2
        return np.sqrt((x+(b*b-a*a)**2*np.sin(theta)**2*np.cos(theta)**2/x))
def C(a,b):
        f1 = lambda x: f(a,b,x)
        return round(integrate.quad(f1, 0,2*np.pi)[0],8)
#  -----------------------------------------------------------------------------
print(C(1,4)+C(3,4))
