from time import time
#  -----------------------------------------------------------------------------
def is_simber(n):
    s = str(n)
    return all(s.count(d)%2 == int(d)%2 for d in set(s))
#  -----------------------------------------------------------------------------
def Q(n):
    count = 0
    for k in range(1,10**n):
        if is_simber(k):
            count += 1
            count %= MOD
    return count
#  -----------------------------------------------------------------------------
MOD = 10**9+123
for exp in range(1,102):
    start = time()
    print(f'For n=10^{exp:3}: {Q(exp):10}, Run-Time: {time()-start:10.3f}')
