from math import log
#  -----------------------------------------------------------------------------
# OEIS: https://oeis.org/A347325
# Solution to the spectator-first Tantalizer problem.
#  -----------------------------------------------------------------------------
def P(n):
    return (-2) ** int(log(n,2)) // 3 + 1 +\
           (n & int('a' * int(log(n, 16) + 1), 16))
#  -----------------------------------------------------------------------------
def S(n):
    if (n == 1):
        return 1
    if (n % 2 == 0):
        return 2*n + n*(n//2) - 4*S(n//2) + 2*P(n//2) - 1
    return 2*n + (n-1)*(n//2 + 1) - 4*S(n//2) - 1
#  -----------------------------------------------------------------------------
def test():
    print(P(1))
    print(P(9))
    print(P(10**3))
    print(S(10**3))
#  -----------------------------------------------------------------------------
MOD = 987654321
test()
print(f'Solution: {S(10**18) % MOD}')
