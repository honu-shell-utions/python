#  -----------------------------------------------------------------------------
#  Sums of totients of powers
#  Problem 512
#  https://projecteuler.net/problem=512
#  -----------------------------------------------------------------------------
from sympy.ntheory.factor_ import totient
from time import time
#  -----------------------------------------------------------------------------
def f(n):
    if n % 2 == 0:
        return 0
    else:
        return totient(n) % (n + 1)
#  -----------------------------------------------------------------------------
def g(n):
    total = 0
    for i in range(1,n+1):
        total += f(i)
    return total
#  -----------------------------------------------------------------------------
for exp in [2,3,4,5,6,7,8]:
    start = time()
    limit = 10**exp
    if exp == 8:
        limit *= 5
    print(f'Solution for n = {limit:10}: {g(limit):18}, Run-Time: {time()-start}')
