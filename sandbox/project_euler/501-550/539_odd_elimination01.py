from time import time
from math import log
#  -----------------------------------------------------------------------------
# OEIS: https://oeis.org/A347325
# Solution to the spectator-first Tantalizer problem.
#  -----------------------------------------------------------------------------
def P(n):
    return (-2) ** int(log(n,2)) // 3 + 1 +\
           (n & int('a' * int(log(n, 16) + 1), 16))
#  -----------------------------------------------------------------------------
def S(n):
    total = 1
    if n == 1:
        return total
    for k in range(2,n+1,2):
        total = (total + 2*P(k)) % MOD
    if n % 2 == 0:
        return total-P(n)
    else:
        return total
#  -----------------------------------------------------------------------------
def test():
    print(P(1))
    print(P(9))
    print(P(10**3))
    print(S(10**3))
#  -----------------------------------------------------------------------------
MOD = 987654321
test()
