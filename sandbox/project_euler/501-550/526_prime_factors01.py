#  -----------------------------------------------------------------------------
#  Largest prime factors of consecutive numbers
#  Problem 526
#  https://projecteuler.net/problem=526
#  -----------------------------------------------------------------------------
from sympy import primefactors
#  -----------------------------------------------------------------------------
def f(n):
    return primefactors(n)[-1]
#  -----------------------------------------------------------------------------
def g(n):
    total = 0
    for k in range(9):
        total += f(n+k)
    return total
#  -----------------------------------------------------------------------------
def h(n):
    max_value = -10**6
    for k in range(2,n+1):
        res = g(k)
        if res > max_value:
            max_value = res
    return max_value
#  -----------------------------------------------------------------------------
print(f(100))
print(f(101))
print(g(100))
print(h(100))
print(h(10**9))

