#-------------------------------------------------------------------------------
## A Scoop of Blancmange
## Problem 226
#-------------------------------------------------------------------------------
from math import sqrt
from scipy.integrate import quad
from time import time
#-------------------------------------------------------------------------------
def f(x):
    return (1 - sqrt(2*x - 4*x*x))/2
#-------------------------------------------------------------------------------
def I(x):
    if x == 1:
        return 1/2
    if 0 < x <= 1/2:
        return I(2*x)/4 + (x*x)/2
    if 1/2 < x <= 1:
        return 1/2 - I(1 - x)
#-------------------------------------------------------------------------------
def euler_226():
    area_under_blancmange = I(1/2) - I(0.0789)
    area_under_circle = quad(f, 0.0789, 1/2)[0]
    return round(area_under_blancmange - area_under_circle, 8)
#-------------------------------------------------------------------------------
start = time()
print(f'Solution: {euler_226()}, Run-Time: {time()-start}')
