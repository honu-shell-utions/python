def f(x, y, l, b):
    q = 0.5 * (x - y)
    z = (q ** 2 + 1) ** 0.5
    if l == 0 and b == 0:
        return z - 0.5 * (x + y)
    if l == 0:
        return f(x, z - q, l, b - 1)
    if b == 0:
        return f(z + q, y, l - 1, b)
    
    return min(f(x, z - q, l, b - 1), f(z + q, y, l - 1, b))
#-------------------------------------------------------------------------------
from time import time
start = time()
k = f(1, 0, 3, 3)
n = 0
s = [(1, 0)]
#-------------------------------------------------------------------------------
while len(s) > 0:
    p = s.pop()
    q = 0.5 * (p[0] - p[1])
    z = (q ** 2 + 1) ** 0.5
    if z - 0.5 * (p[0] + p[1]) > k:
        n += 1
        s += [(p[0], z - q), (z + q, p[1])]

print(f'Solution: {n + 1}, Run-Time: {time()-start}')
