from sympy import divisors
#-------------------------------------------------------------------------------
def div_sum(n):
    return sum(divisors(n)) 
#-------------------------------------------------------------------------------
def perfect_quot(n):
    return div_sum(n)/n
#-------------------------------------------------------------------------------
limit = 10**6
n_sum = 0
for n in range(2,limit+1,2):
    result = perfect_quot(n) -.5
    if int(result) == result:
        n_sum += n
        print(n,result,n_sum)

