import numpy as np
#  -----------------------------------------------------------------------------
def M(base,d):
    #get d in base
    d = np.base_repr(d,base)
    m = 1
    #loop until hit
    while True:
        #m squared converted to base
        mbase = np.base_repr(m**2,base)
        #is d in mbase
        if str(d) in str(mbase):
            return m
        else:
            m += 1
#  -----------------------------------------------------------------------------
print(M(10,7))
print(M(11,10))
