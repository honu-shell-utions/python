#  -----------------------------------------------------------------------------
#  x**y = y**x (mod n)
#  Problem 801
#  https://projecteuler.net/problem=801
#  -----------------------------------------------------------------------------
from time import time
from sympy import primerange
#  -----------------------------------------------------------------------------
def f(n):
    count = 0
    for x in range(1,n**2-n+1):
        for y in range(1,n**2-n+1):
            if x == y:
                count += 1
                continue
            lhs = pow(x,y,n)
            rhs = pow(y,x,n)
            if lhs == rhs:
                count += 1
    return count
#  -----------------------------------------------------------------------------
def S(M,N):
    total = 0
    for p in primerange(M,N):
        total += f(p)
    return total % MOD
#  -----------------------------------------------------------------------------
MOD = 993353399
print(f(5))
print(f(97))
print(S(1,10**2))
print(S(1,10**5))
