#  -----------------------------------------------------------------------------
#  Square the Smallest 
#  Problem 822
#  https://projecteuler.net/problem=822
#  -----------------------------------------------------------------------------
from time import time
#  -----------------------------------------------------------------------------
def S(n,m):
    k = [i for i in range(2,n+1)]
    while k[0]**2 <= k[-1]:
        k[0] = k[0]**2
        k = sorted(k)
        m -= 1

    ans = 0
    for i in range(len(k)):
        if i < m % len(k):
            ans += pow(k[i],pow(2,m // len(k)+1,MOD-1),MOD)
        else:
            ans += pow(k[i],pow(2,m // len(k),MOD-1),MOD)
    return ans % MOD
#  -----------------------------------------------------------------------------
MOD = 1234567891
for n,m in [(5,3),(10,100),(10**4,10**16)]:
    start = time()
    print(f'Solution for (n,m) = ({n:5},{m:17}): {S(n,m):10}, Run-Time: {time()-start:.3f}')
