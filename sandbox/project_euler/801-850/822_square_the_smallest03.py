#  -----------------------------------------------------------------------------
#  Square the Smallest 
#  Problem 822
#  https://projecteuler.net/problem=822
#  -----------------------------------------------------------------------------
from time import time
from math import sqrt
#  -----------------------------------------------------------------------------
def S(n,m):
    L = list(range(2, n + 1))
    #the Walrus operator
    while (i := min(L)) ** 2 <= n:
        L[L.index(i)] **= 2
        m -= 1
    L = sorted(L)
    q, r = divmod(m, n - 1)
    return (sum(pow(L[i], pow(2, q + (i < r), MOD - 1), MOD) for i in range(n - 1)) % MOD)
#  -----------------------------------------------------------------------------
MOD = 1234567891
for n,m in [(5,3),(10,100),(10**4,10**16)]:
    start = time()
    print(f'Solution for (n,m) = ({n:5},{m:17}): {S(n,m):10}, Run-Time: {time()-start:.3f}')
