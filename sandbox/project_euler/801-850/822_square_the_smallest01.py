from time import time
#  -----------------------------------------------------------------------------
def S(n,m):
    n_list = [x for x in range(2,n+1)]
    for _ in range(m):
        n_list[0] = n_list[0]**2
        n_list = sorted(n_list)
    return sum(n_list) % 1234567891
#  -----------------------------------------------------------------------------

#  -----------------------------------------------------------------------------
print(S(5,3))
print(S(10,100))
print(S(10**4,10**16))
