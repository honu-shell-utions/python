from math import sqrt, ceil
#  -----------------------------------------------------------------------------
def M(d):
    # 1.
    m = pow(d, (p+1)//4, p)
    m2 = m*m % p
    if m2 == p - d:
        return min(m, p-m)
    # 2.
    if d % 2 == 0 and (d//2)**2 < p:
        return p - d//2
    #3. 
    m2 = p2 - p*d
    while True:
        m = ceil(sqrt(m2))
        if m*m - m2 < p:
            return m
        m2 += p2
#  -----------------------------------------------------------------------------
p = 10**9 + 7
N = 10**5
p2 = p*p
print(sum(M(d) for d in range(1, N+1)))
