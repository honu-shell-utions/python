from time import time
import numpy as np
#  -----------------------------------------------------------------------------
def g(n):
    res = 40+474*n + 1835*n**2 + 2085*n**3 + 765*n**4 + 81*n**5
    res = 7**n*res//40
    return res
#  -----------------------------------------------------------------------------
start = time()
res = g(142857)
res = np.base_repr(res,7)
print(f'Solution: {str(res)[:10]}, Run-Time: {time()-start:.3f}')
