from math import floor
## -----------------------------------------------------------------------------
def rand48(n):
    rnd = 123456
    for k in range(1,n+1):
        rnd = (25214903917*rnd + 11) % 2**48
    return rnd
## -----------------------------------------------------------------------------
def make_letters():
    letters = []
    for asc in range(97,123):
        letters.append(chr(asc))
    for asc in range(65,91):
        letters.append(chr(asc))
    return letters
## -----------------------------------------------------------------------------
def make_string(n):
    temp = ''
    for i in range(n):
        rnd = rand48(i)
        b = floor(rnd/2**16) % 52
        c = letters[b]
        temp += c
    return temp
