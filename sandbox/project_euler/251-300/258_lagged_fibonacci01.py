## -----------------------------------------------------------------------------
## A lagged Fibonacci sequence
## Problem 258
## A sequence is defined as:
## 
## gsub(k) = 1, for 0 ≤ k ≤ 1999
## gsub(k) = gsub(k-2000) + gsub(k-1999), for k ≥ 2000.
## Find gsub(k) mod 20092010 for k = 10^18.
## -----------------------------------------------------------------------------

## -----------------------------------------------------------------------------
def lfs(SIZE):
    lst = []
    for k in range(2000):
        lst.append(1)
    for k in range(2001,SIZE+1):
        lst.append(lst[k-2000] + lst[k-1999])
    return lst
## -----------------------------------------------------------------------------
SIZE = 10**6
test = lfs(SIZE)
mod = 20092010
print(test[-1] % mod)
