#  -----------------------------------------------------------------------------
#  Sum of digits
#  Problem 294
#  https://projecteuler.net/problem=294
#  -----------------------------------------------------------------------------
from time import time
#  -----------------------------------------------------------------------------
def d23(k):
    total = 0
    while k != 0:  
        total = total + (k % 10)
        if total > 23:
            return False
        k = k // 10
    return total == 23
#  -----------------------------------------------------------------------------   
def S(n):
    count = 0
    # 1679 is the smallest integer with digit sum of 23 that is also divisible by 23 
    for k in range(1679,10**n,23):
        if k % 9 != 5:
            continue
        if d23(k):
            count += 1
    return count
#  -----------------------------------------------------------------------------
for exp in [6,9,12,15]:
    start = time()
    print(f'Solution for n = 10^{exp:<2}: {S(exp):15}, Run-Time: {time()-start}')
