#  -----------------------------------------------------------------------------
#  Zeckendorf Representation
#  Problem 297
#  https://projecteuler.net/problem=297
#  -----------------------------------------------------------------------------
from time import time
#  -----------------------------------------------------------------------------
f = [1, 2]
s = [1, 2]

nmax = 10 ** 17 - 1

while True:
    ff = f[-2] + f[-1]
    if ff > nmax:
        break
    ss = s[-2] + s[-1] + f[-2] - 1
    f.append(ff)
    s.append(ss)

c = 0
n = nmax
for i in reversed(range(len(f))):
    if n >= f[i]:
        c += s[i] + n - f[i]
        n -= f[i]

print(c)
