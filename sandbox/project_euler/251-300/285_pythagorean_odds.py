#  -----------------------------------------------------------------------------
#  Pythagorean odds
#  Problem 285
#  https://projecteuler.net/problem=285
#  -----------------------------------------------------------------------------
from math import sqrt, asin
#  -----------------------------------------------------------------------------
def a(d, r):
    if d * sqrt(2) > r:
        return 0
    x = sqrt(r ** 2 - d ** 2) - d
    return r ** 2 * asin(x / r / sqrt(2)) - d * x
#  -----------------------------------------------------------------------------
def aa(k):
    return k * (a(1 / k, 1 + 1 / 2 / k) - a(1 / k, 1 - 1 / 2 / k))
#  -----------------------------------------------------------------------------
solution = sum(aa(k) for k in range(1, 100001))
print(f'{round(solution,5)}')
