#  -----------------------------------------------------------------------------
#  Angular Bisector and Tangent
#  Problem 296
#  https://projecteuler.net/problem=296
#  -----------------------------------------------------------------------------
from math import gcd
from time import time
#  -----------------------------------------------------------------------------
start = time()
nmax = 10**5
s = 0
for a in range(1, nmax // 3 + 1):
    for b in range(a, (nmax - a) // 2 + 1):
        g = gcd(a, b)
        c = (a + b) // g
        d1 = (b + c - 1) // c
        d2 = min(a + b - 1, nmax - a - b) // c
        if d2 >= d1:
            s += d2 - d1 + 1

print(f'Solution: {s}, Run-Time: {time()-start}')
