def bin_to_dec(s):
    n = 0
    for x in s: 
        n *= 2
        if x == '1':
            n += 1
    return n
#  -----------------------------------------------------------------------------
def f(n,s):
    global solution
    if len(s) == 2**n:
        for i in range(1,n):
            if s[-i:]+s[:n-i] in s:
                return 0
        solution += bin_to_dec(s)
    for x in ['0','1']:
        if s[-n+1:]+x not in s:
            f(n,s+x)
