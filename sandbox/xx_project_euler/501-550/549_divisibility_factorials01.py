#  -----------------------------------------------------------------------------
#  Divisibility of factorials
#  Problem 549
#  https://projecteuler.net/problem=549
#  -----------------------------------------------------------------------------
from math import factorial
from time import time
#  -----------------------------------------------------------------------------
def s(n):
    i = 2
    while True:
        if factorial(i) % n == 0:
            return i
        i += 1
    return None
#  -----------------------------------------------------------------------------
def S(n):
    total = 0
    for i in range(2,n+1):
        total += s(i)
    return total
#  -----------------------------------------------------------------------------
start = time()
limit = 10**2
print(f'Solution: {S(limit)}, Run-Time: {time()-start}')
