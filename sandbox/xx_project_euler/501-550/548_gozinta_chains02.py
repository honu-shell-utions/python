def D(n):
    f=set()
    i=2
    while i*i<=n:    
        if n%i==0:
            f|={i}
            f|={n//i}
        i+=1
    return f
#  -----------------------------------------------------------------------------
g=[0,0,1,1]
for n in range(4,10**5):
    g.append(sum([g[f] for f in D(n)])+1)

sols = [1]
for i in range(1,len(g)):
    if i == g[i]:
        sols.append(g[i])
print(f'Beginning of OEIS sequence:\n {sols}')
        
