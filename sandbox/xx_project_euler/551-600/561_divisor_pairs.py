#  Divisor Pairs
#  Problem 561
#  https://projecteuler.net/problem=561
#  -----------------------------------------------------------------------------
def ruler_sum(n):
    if n == 0:
        return 0
    return ruler_sum(n//2) + n//4
#  -----------------------------------------------------------------------------
def Q(n):
    return (904961+1)*ruler_sum(n)
#  -----------------------------------------------------------------------------
for n in [8,10**3,10**9,10**12]:
    print(f'For n = {n:14}: {Q(n)}')
