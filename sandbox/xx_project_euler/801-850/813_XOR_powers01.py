from time import time
#  -----------------------------------------------------------------------------
def x_or_mult(a,b):
    results = 0
    while b != 0:
        results ^= a * (b & 1)
        a <<= 1
        b >>= 1
    return results
#  -----------------------------------------------------------------------------
def P(exp):
    res = 1
    for _ in range(exp):
        res = x_or_mult(res,11)
    return res % MOD
#  -----------------------------------------------------------------------------
MOD = 10**9+7
for n in range(2,101):
    print(n,P(n))
