#  -----------------------------------------------------------------------------
#  Marsh Crossing
#  Problem 607
#  https://projecteuler.net/problem=607
#  -----------------------------------------------------------------------------
from math import sqrt
from scipy.optimize import minimize
from time import time
#  -----------------------------------------------------------------------------
def first_seg(d):
    return sqrt(x**2 + d**2 + sqrt2*x*d)
#  -----------------------------------------------------------------------------
def middle_seg(d1, d2):
    d12 = d1 - d2
    return sqrt(d12**2 + y**2 - sqrt2*y*d12)
#  -----------------------------------------------------------------------------
def last_seg(d):
    return sqrt(d**2 + x**2 - sqrt2*x*d)
#  -----------------------------------------------------------------------------
def C(x):
    return first_seg(x[0])/10 +\
           middle_seg(x[0],x[1])/9 +\
           middle_seg(x[1],x[2])/8 +\
           middle_seg(x[2],x[3])/7 +\
           middle_seg(x[3],x[4])/6 +\
           middle_seg(x[4],x[5])/5 +\
           last_seg(x[5])/10
#  -----------------------------------------------------------------------------
start = time()
sqrt2 = sqrt(2)
x = 50 - 25*sqrt2
y = 10*sqrt2
min_time = minimize(C, [0,0,0,0,0,0], tol=1e-10)
print(f'Solution: {round(min_time.fun,10)} days, Run-Time: {time()-start:8.3f}')
