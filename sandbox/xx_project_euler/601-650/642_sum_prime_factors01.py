#  -----------------------------------------------------------------------------
#  Sum of largest prime factors
#  Problem 642
#  https://projecteuler.net/problem=642
#  -----------------------------------------------------------------------------
from sympy import primefactors
from time import time
#  -----------------------------------------------------------------------------
def f(n):
    return primefactors(n)[-1]
#  -----------------------------------------------------------------------------
def F(n):
    total = 0
    for i in range(2,n+1):
        total += f(i)
        total = total % MOD
    return total
#  -----------------------------------------------------------------------------
MOD = 10**9
for n in [10,10**2,10**4,201820182018]:
    start = time()
    print(f'Solution for n = {n}: {F(n)}, Run-Time: {time()-start:.3f}')
