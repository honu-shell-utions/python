#  -----------------------------------------------------------------------------
#  Numbers of the form a^2*b^3
#  Problem 634
#  https://projecteuler.net/problem=634
#  -----------------------------------------------------------------------------
from sympy import factorint
from time import time
#  -----------------------------------------------------------------------------
def F(n):
    powerful = []
    count = 0
    for a in range(2,int((9*n/2**3)**(1/2))+1):
        for b in range(2,int((9*n/2**2)**(1/3))+1):
            x = a**2*b**3
            if x > n:
                break
            if x not in powerful:
                powerful.append(x)
                count += 1
    return count
#  -----------------------------------------------------------------------------
for txt,limit in [('10^2',10**2),('2*10^4',2*10**4),('3*10^6',3*10**6),('9*10^18',9*10**18)]:
    start = time()
    powerful = F(limit)
    print(f'Solution for n = {txt:>8} -----> {powerful:11}, Run-Time: {round(time()-start,5)}')
