from time import time
#  -----------------------------------------------------------------------------
def A(k,n):
    total = 0
    f0 = 1
    power = pow(2, n//k, MOD)
    for a in range(0, n//k + 1):
        total = (total + (f0 * pow(power, k-2*a, MOD))) % MOD
        f0 = (f0*(k-2*a)*(k-2*a-1)) % MOD
        f0 = (f0*pow((a+1)**2, -1, MOD)) % MOD
    return total
#  -----------------------------------------------------------------------------
MOD = 1000000007
for k,n in [(3,9),(4,20),(16,32),(10**2,10**4),(10**4,10**8),(10**8,10**16)]:
    start = time()
    print(f'Solution for ({k:8,},{n:17,}): {A(k,n):14,}, Run-Time: {time()-start:.3f}')
