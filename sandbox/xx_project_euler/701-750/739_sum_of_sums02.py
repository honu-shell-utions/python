#  -----------------------------------------------------------------------------
#  Summation of Summations
#  Problem 739
#  https://projecteuler.net/problem=739
#  https://oeis.org/A001517/a001517.pdf
#  -----------------------------------------------------------------------------
from time import time
#  -----------------------------------------------------------------------------
def series(n):
    a, b, s = 1, 3, []
    for _ in range(n):
        a, b = b, (a + b) % MOD
        s.append(a)
    return s
#  -----------------------------------------------------------------------------
def inv(n):
    return pow(n,-1,MOD)
#  -----------------------------------------------------------------------------
def f(n):
    fact = [1] * (2*n)
    for i in range(1, 2*n):
        fact[i] = (fact[i - 1] * i) % MOD

    total = 0
    ser = series(n - 1)
    const = (inv(fact[n - 2]) * inv(n - 1)) % MOD
    for k in range(n - 1):
        total += fact[n + k - 2] * inv(fact[k]) * \
                (n - k - 1) * ser[n - k - 2] * const

    return total % MOD
#  -----------------------------------------------------------------------------
MOD = 10**9 + 7
for n in [8,20,10**8]:
    start = time()
    print(f'Solution for n = {n:10}: {f(n):10}, Run-Time: {(time()-start)/60:5.3f} minutes.')
