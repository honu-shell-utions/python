#  -----------------------------------------------------------------------------
#  Factors of Two in Binomial Coefficients
#  Problem 704
#  https://projecteuler.net/problem=704
#  -----------------------------------------------------------------------------
from math import factorial
from sympy import factorint
#  -----------------------------------------------------------------------------
def C(n,r):
    return factorial(n)//(factorial(n-r)*factorial(r))
#  -----------------------------------------------------------------------------
def g(n,r):
    res = C(n,r)
    if res % 2 != 0:
        return 0
    x = factorint(res)
    return x[2]
#  -----------------------------------------------------------------------------
def F(n):
    temp = []
    for m in range(n+1):
        temp.append(g(n,m))
    return max(temp)
#  -----------------------------------------------------------------------------
def S(N):
    total = 0
    for n in range(1,N+1):
        total += F(n)
    return total
#  -----------------------------------------------------------------------------
for limit in range(100,10**4,100):
    print(limit,'----------->',S(limit))
