#  -----------------------------------------------------------------------------
#  Lattice points enclosed by parabola and line
#  Problem 403
#  https://projecteuler.net/problem=403
#  -----------------------------------------------------------------------------
from math import isqrt, sqrt
from time import time
#  -----------------------------------------------------------------------------
def L(z):
  res = (z*(z*z + 5))//6 + 1
  return res
#  -----------------------------------------------------------------------------
def S(n):
  a = n*(n+1)
  return (a*(a + 10))//12 + 2*(n + 1)
#  -----------------------------------------------------------------------------
def euler403(N):
  K = isqrt(N) 
  res = S(N) - 2*L(N-1) - 1
  for i in range(1,K+1):
    Ni = N//i
    res += S(Ni-i) + S(Ni+i) - S(2*i-1) - L(2*i) 
    res %= MOD
  return res
#  -----------------------------------------------------------------------------
MOD = 10**8
start = time()
print(f'For N = 5: {euler403(5):15}, Run-Time: {time()-start:.3f}')
for exp in range(2,13):
    start = time()
    N = 10**exp
    print(f'For N = 10^{exp:3}: {euler403(N):10}, Run-Time: {time()-start:.3f}')
