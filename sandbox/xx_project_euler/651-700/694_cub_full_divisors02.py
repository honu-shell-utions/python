#  -----------------------------------------------------------------------------
#  Cube-full Divisors
#  Problem 694
#  https://projecteuler.net/problem=694
#  -----------------------------------------------------------------------------
from sympy import primerange
from time import time
#  -----------------------------------------------------------------------------
def S(N,i):
    answer = 0
    while i < len(PRIMES) and PRIMES[i]**3 <= N:
        p = PRIMES[i]
        q = p**3
        while q <= N:
            m = N//q
            answer += m + S(m,i+1)
            q *= p
        i += 1
    return answer
#  -----------------------------------------------------------------------------
for N in [16,10**2,10**4,10**5,10**6,10**7,10**18]:
    start = time()
    PRIMES = list(primerange(1,N**(1/3)))
    print(f'Solution for N = {N:19}: {N+S(N,0):19}, Run-Time: {time()-start:.3f}')
