#  -----------------------------------------------------------------------------
#  Largest prime
#  Problem 659
#  https://projecteuler.net/problem=659
#  -----------------------------------------------------------------------------
from time import time
#  -----------------------------------------------------------------------------
def euler_659(N=10**7, MOD=10**18):
    L = [4*n*n + 1 for n in range(N+1)]
    M = [0] * (N+1)
    for i, p in enumerate(L):
        if p != 1:  # then it's a prime
            M[i] = p
            if p-i <= N:
                for start in (p-i, p+i):
                    for j in range(start, N+1, p):
                        while not L[j] % p:
                            L[j] //= p
                        M[j] = max(M[j], p)
    return sum(M) % MOD
#  -----------------------------------------------------------------------------
for exp in range(2,8):
    start = time()
    limit = 10**exp
    print(f'For n = 10^{exp}: {euler_659(limit):>18}, Run-Time: {time()-start}')
