#  -----------------------------------------------------------------------------
#  Ruff Numbers
#  Problem 773
#  https://projecteuler.net/problem=773
#  -----------------------------------------------------------------------------
from sympy import sieve
from math import prod, gcd
#  -----------------------------------------------------------------------------
def build_7s():
    primes = [2,5]
    for p in sieve:
        if p % 10 == 7:
            primes.append(p)
            if len(primes) == NUM_PRIMES:
                return primes
#  -----------------------------------------------------------------------------
def F(k):
    p = prod(primes[:k+2])
    total = 0
    for i in range(7,p,10):
        if gcd(p,i) != 1:
            continue
        total = (total + i) % MOD
    return total
#  -----------------------------------------------------------------------------
NUM_PRIMES = 100
MOD = 10**9 + 7
primes = build_7s()
for K in [3,4,5,97]:
    print(K,F(K))
