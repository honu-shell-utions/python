#  -----------------------------------------------------------------------------
#  Alternating GCD Sum
#  Problem 795
#  https://projecteuler.net/problem=795
#  -----------------------------------------------------------------------------
from math import log
from time import time
#  -----------------------------------------------------------------------------
def h(p,e):
    if p==2:
        if e&1:
            return (3<<e)*((1<<(e+1)//2)-1)
        else:
            return (1<<e)*((1<<e//2+2)-3)
    else:
        if e&1:
            return p**(e-1)*(2*p**((e+1)//2)-1)
        else:
            return p**(e-1)*(p**(e//2+1)+p**(e//2)-1)
#  -----------------------------------------------------------------------------
def euler_795(N):
    N = N >> 1
    hli=[1]*(N+1)      
    for p in range(2,N+1):
        if hli[p] == 1:
            hli[p] = fac = h(p,1)
            for j in range(2*p,N+1,p):
                hli[j]*=fac
            for e in range(2,int(log(N)/log(p))+1):
                for j in range(p**e,N+1,p**e):
                    hli[j]=hli[j]*h(p,e)//h(p,e-1)
    return(sum(hli[1:])-N*N)
