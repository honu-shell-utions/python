# -------------------------------------------
from time import time
go = time()
# -------------------------------------------
def T60(n):
    t = (2*n**3 + 5*n**2 + 2*n)//8 + (2*(n**3 - n/3))//2
    return t                                       
# -------------------------------------------
def T90(n):
    t = n*(n+1)*(n+2)//6
    t += (2*n**3 + 5*n**2 + 2*n)//8
    t += (2*n**3 + 3*n**2 - 3*n)//18
    t += (2*n**3 + 3*n**2 - 3*n)//10
    t *= 6
    return t
# -------------------------------------------
def T120(n):
    t = 3*(22*n**3 + 45*n**2 - 4*n)//48
    return t
# -------------------------------------------
def T(n):
    return T60(n) + T90(n) + T120(n)
# -------------------------------------------
print (f'Solution: {round(T(36))}, Run-Time: {round(time()-go,2)}')
