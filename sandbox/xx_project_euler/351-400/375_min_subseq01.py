#  -----------------------------------------------------------------------------
def M(N):
    total = 0
    for i in range(1,N+1):
        for j in range(i,N+1):
            total += A(i,j)
    return total
#  -----------------------------------------------------------------------------
def build_pseudo_list(n):
    pr_list = [0]
    pr = pseudo_rand()
    while len(pr_list) < n+1:
        p = next(pr)
        pr_list.append(p)
    return pr_list
